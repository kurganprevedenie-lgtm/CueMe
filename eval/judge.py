"""LLM-судья: оценивает сгенерированный ответ по рубрике одним вызовом.
Даёт числовой балл 0-10 + короткий вердикт — для объективного A/B версий промпта.
Использует тот же провайдерный слой, что и прод (llm._ask).
"""
import re

import llm

_SCORE_RE = re.compile(r"БАЛЛ\s*:?\s*(\d{1,2})")


def _context(sc: dict) -> str:
    if sc["kind"] == "rewrite":
        return f"Автор написал черновик: «{sc['draft']}». Его переписали, чтобы звучало живее в его же голосе."
    return f"Собеседник написал автору: «{sc['incoming']}». Нужен ответ в голосе автора."


async def score(sc: dict, generated: str) -> tuple[int, str]:
    """Возвращает (балл 0-10, однострочный вердикт)."""
    prompt = (
        "Ты — строгий эксперт по дейтинг-переписке. Оцени ОДИН сгенерированный ответ.\n"
        "Контекст: автор пишет коротко, с иронией, на «ты», строчными, без лишних эмодзи. "
        "Собеседник любит конкретику и лёгкий юмор, не любит длинные простыни и формальный тон.\n\n"
        f"Ситуация: {_context(sc)}\n"
        f"Сгенерированный ответ: «{generated}»\n\n"
        "Оцени по рубрике (каждый пункт вносит вклад в общий балл), думая про себя, "
        "но не выводя рассуждений:\n"
        "1) В голосе автора (коротко, на «ты», без ИИ-штампов и формальщины)\n"
        "2) Не навязчиво, с самоуважением (на отказ/холод — достоинство, без выпрашивания)\n"
        "3) Цепляется за конкретную деталь входящего, не общая фраза\n"
        "4) Тепло и живо, звучит как настоящий человек\n"
        "5) Уместность под ситуацию (уязвимость → поддержка, а не совет/дожим)\n\n"
        "Выведи РОВНО две строки:\n"
        "БАЛЛ: <число 0-10>\n"
        "ВЕРДИКТ: <одно короткое предложение по-русски, главный плюс или минус>"
    )
    raw = await llm._ask(prompt, max_tokens=200)
    m = _SCORE_RE.search(raw or "")
    score_val = int(m.group(1)) if m else 0
    score_val = max(0, min(10, score_val))
    verdict = ""
    for line in (raw or "").splitlines():
        if "ВЕРДИКТ" in line.upper():
            verdict = line.split(":", 1)[-1].strip()
            break
    return score_val, verdict or "(без вердикта)"
