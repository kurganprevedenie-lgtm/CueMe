async def rewrite_message(draft: str, style_card: str, interaction_card: str) -> str:
    """Переписывает черновик в голосе пользователя под конкретного собеседника."""
    prompt = (
        "Ты переписываешь сообщение от лица конкретного человека — под конкретного собеседника.\n\n"
        f"ГОЛОС АВТОРА (style_card):\n{style_card}\n\n"
        f"СОБЕСЕДНИК — его привычки и что у него заходит (interaction_card):\n{interaction_card}\n\n"
        f"ЧЕРНОВИК АВТОРА:\n{draft}\n\n"
        "=== ЧТО СОХРАНЯЕМ (голос автора из style_card) ===\n"
        "• Личный словарный запас — его слова и обороты, не чужие\n"
        "• Смысл и суть — не меняй что он хочет сказать\n"
        "• Регистр — если пишет с маленькой буквы, оставь маленькую\n"
        "• Эмодзи — если в черновике нет, не добавляй\n\n"
        "=== ЧТО МЕНЯЕМ (адаптация под собеседника из interaction_card) ===\n"
        "• Длина — держи ПРИМЕРНО как в черновике. Не сокращай резко, не делай телеграфным\n"
        "• Тон и теплота — под то, что у него даёт живой отклик\n"
        "• Первая фраза / заход — измени чтобы цеплял этого человека\n"
        "• Формальность — под его уровень: ты/Вы, официально/неформально\n"
        "• Ритм и структура — под его привычки читать и отвечать\n\n"
        "=== КРИТЕРИИ ===\n"
        "ПРОВАЛ — только исправлена орфография/пунктуация, подача та же.\n"
        "УСПЕХ — изменён тон, заход, формальность и ритм под человека, "
        "при этом длина близка к черновику.\n\n"
        "ПРИМЕРЫ ожидаемой глубины:\n"
        "Черновик: «хотел спросить, ты не забыл про встречу в пятницу?»\n"
        "→ Теплее: «слушай, ты же помнишь про встречу в пятницу?»\n"
        "→ Формально: «напоминаю про нашу встречу в пятницу, всё в силе?»\n\n"
        "Верни только итоговое переписанное сообщение — без кавычек, без пояснений."
    )
    return await _ask(prompt)


_DELIM = "===ПОЯСНЕНИЕ==="


def _split_explained(raw: str) -> tuple[str, str]:
    if _DELIM in raw:
        msg, expl = raw.split(_DELIM, 1)
        return msg.strip(), expl.strip()
    return raw.strip(), ""


async def rewrite_message_explained(
    draft: str, style_card: str, interaction_card: str
) -> tuple[str, str]:
    """Как rewrite_message, но дополнительно объясняет что изменено. Один вызов LLM."""
    prompt = (
        "Ты переписываешь сообщение от лица конкретного человека — под конкретного собеседника.\n\n"
        f"ГОЛОС АВТОРА (style_card):\n{style_card}\n\n"
        f"СОБЕСЕДНИК — его привычки и что у него заходит (interaction_card):\n{interaction_card}\n\n"
        f"ЧЕРНОВИК АВТОРА:\n{draft}\n\n"
        "=== ЧТО СОХРАНЯЕМ ===\n"
        "• Личный словарный запас, смысл, регистр. Эмодзи не добавляй если их нет.\n"
        "• Длину — держи ПРИМЕРНО как в черновике, не сокращай резко.\n"
        "=== ЧТО МЕНЯЕМ под собеседника ===\n"
        "• Тон, первую фразу, формальность, ритм.\n\n"
        f"Сначала выведи ТОЛЬКО переписанное сообщение — без кавычек.\n"
        f"Затем на отдельной строке: {_DELIM}\n"
        "Затем 1-2 коротких фразы что именно ты изменил под этого собеседника "
        "(например: «сократил вдвое, убрал формальность, заход сделал теплее»)."
    )
    return _split_explained(await _ask(prompt))


async def suggest_reply(
    incoming_msg: str, style_card: str, interaction_card: str
) -> tuple[str, str]:
    """Предлагает как ответить на сообщение собеседника — в голосе автора."""
    prompt = (
        "Собеседник прислал автору сообщение. Предложи КАК ответить ему — "
        "в голосе автора, с учётом привычек собеседника.\n\n"
        f"ГОЛОС АВТОРА (style_card):\n{style_card}\n\n"
        f"СОБЕСЕДНИК (interaction_card):\n{interaction_card}\n\n"
        f"СООБЩЕНИЕ СОБЕСЕДНИКА:\n{incoming_msg}\n\n"
        "ПРАВИЛА:\n"
        "• Ответ в стиле автора: его слова, регистр, длина под собеседника\n"
        "• Не выдумывай факты, которых автор знать не может\n"
        "• Если по сообщению нужна конкретика которой нет — предложи короткий уточняющий ответ\n"
        "• Это черновик ответа от лица автора, а не совет со стороны\n\n"
        f"Сначала выведи ТОЛЬКО текст ответа — без кавычек.\n"
        f"Затем на отдельной строке: {_DELIM}\n"
        "Затем 1 короткую фразу почему ответ такой."
    )
    return _split_explained(await _ask(prompt))
